# 각 모집단의 모분산을 표본분산과 같다고 가정

# H0 : 두 펀드의 연평균 수익률 차이 X
# H1 : 두 펀드의 연평균 수익률 차이 O
# a  : 0.05
# T0 : 영가설 하에서의 검정 통계량
fundA <- c(8,12,2)
fundB <- c(10,8,3)
a <- 0.05
t <- function(x,y,a){
  t0 <- (x[2]-y[2])/sqrt((x[3]^2/x[1])+(y[3]^2/y[1]))
  if(abs(t0)>1.96){
    cat('영가설 기각')
  }else{
    cat('영가설 채택')
  }
}
t(fundA,fundB,a)
