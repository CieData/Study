# A 모집단
# 표본 크기 100
# 표본 평균 4.5
# 모분산 4
A <- c(100,4.5,4)
mu <- function(A, a=0){
  if(a==95){
  cat('신뢰수준 95%의 신뢰구간\n')
  cat(A[2] - 1.96*(sqrt(A[3])/sqrt(A[1])))
  cat('<= 모평균 <=')
  cat(A[2] + 1.96*(sqrt(A[3])/sqrt(A[1])))
  }else if(a==99){
  cat('신뢰수준 99%의 신뢰구간\n')
  cat(A[2] - 2.58*(sqrt(A[3])/sqrt(A[1])))
  cat('<= 모평균 <=')
  cat(A[2] + 2.58*(sqrt(A[3])/sqrt(A[1])))
  }else{
  cat('신뢰수준 95%의 신뢰구간\n')
  cat(A[2] - 1.96*(sqrt(A[3])/sqrt(A[1])))
  cat('<= 모평균 <=')
  cat(A[2] + 1.96*(sqrt(A[3])/sqrt(A[1])))
  cat('\n\n신뢰수준 99%의 신뢰구간\n')
  cat(A[2] - 2.58*(sqrt(A[3])/sqrt(A[1])))
  cat('<= 모평균 <=')
  cat(A[2] + 2.58*(sqrt(A[3])/sqrt(A[1])))
  }
}
mu(A,95)
