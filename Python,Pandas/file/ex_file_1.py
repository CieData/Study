# --------------------------------------------------------------------

# [파일 쓰기] ========================================================
FILE_NAME='data.txt'

# (1) 파일 열기(파일경로 + 파일명 , 모드, 인코딩)
fp=open(FILE_NAME,mode='w',encoding='utf-8')

# (2) 쓰기 => write('데이터')
fp.write('good\n')
fp.write('happy')

# (3) 닫기 => close()
fp.close()

