# --------------------------------------------------------------------
# 파일 읽기 쓰기 => 파일 입출력(I/O)
# --------------------------------------------------------------------
# (1) 열기 open     => 동작모드 mode='w' 쓰기, 'r' 읽기
# (2) 쓰기 & 읽기
